add=function(x,y){
    return x+' '+y
}



len=function(x){
    return x.length
}
lcase=function(x){
    return x.toLowerCase()
}
ucase=function(x){
    return x.toUpperCase()
}

module.exports.add=add;
module.exports.len=len;
module.exports.lcase=lcase;
module.exports.ucase=ucase;